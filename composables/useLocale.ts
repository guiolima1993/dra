export function useLocale() {
  const locale = _locale

  function setLocale(l: Locale) {
    locale.value = l
  }

  function t(key: string): string {
    return translations[locale.value]?.[key] ?? translations.pt?.[key] ?? key
  }

  return { locale, setLocale, t }
}
